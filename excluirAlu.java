    private void verAExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verAExcluirActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = conexao.con();
            PreparedStatement ps = con.prepareStatement("select matricula, nome, turma, faltas from aluno");
            ResultSet res = ps.executeQuery();
            DefaultTableModel md = (DefaultTableModel) tabela1.getModel();
            while(tabela1.getRowCount()>0){
                md.removeRow(0);
            }
            while(res.next()){
                String[] itens = new String[]{String.valueOf(res.getInt("matricula")), res.getString("nome"),
                res.getString("turma"), String.valueOf(res.getInt("faltas"))};
                md.addRow(itens);
            }
        }catch(Exception ex){   
        }
    }//GEN-LAST:event_verAExcluirActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Connection con = conexao.con();
            PreparedStatement ps = con.prepareStatement("delete from aluno where matricula = ?");
            ps.setInt(1, Integer.valueOf(cMat.getText()));
            int num = ps.executeUpdate();
            if(num>0){
                JOptionPane.showMessageDialog(null, "Aluno deletado com sucesso!!", "Mensagem de exclus찾o", 
                        JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Algo deu errado ao exclir o aluno", "ERRO!!", 
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

